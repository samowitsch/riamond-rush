function Rand(n1, n2) {
    return Math.floor(Math.random() * n2) + n1
}

function Rand1ToMinus1() {
    return Math.random() * (1 - -1) + -1
}

function Min(x, y) {
    if (x < y)
        return x
    return y
}

function Max(x, y) {
    if (x > y)
        return x
    return    y
}
